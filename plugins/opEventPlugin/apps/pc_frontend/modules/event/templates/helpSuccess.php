P1. Login<br>
P2. Edit your profile<br>
P3. If you have a band, create a band<br> 
P4. Share your id or band id (you are going to perform) with the event organizer. They will input in step O3.<br>
Sorry for this current inconvenient user interface<br>
P5. Create a new tune<br>
