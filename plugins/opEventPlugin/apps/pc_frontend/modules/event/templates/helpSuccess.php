<br><b>This is a Music Event Networking Service.</b><br><br> 
Organizer can only input event information and players lineup.<br>
Player can create band, input their profile, and information about the songs they are going to perform. <br>
<br>
You can access to any contents without logging in. You can log in by twitter/facebook account to edit.<br>

<br>
<div class="partsHeading">Organizer</div>
<li>Create an event and connect with people</li><br>
O1. Login<br>
O2. Create an event<br>
O3. Edit information of created event<br>
O4. Share url of the event<br>
<br>
<li>Lineup Configuration in event information</li><br>
Input artist id, text and duration list as following. By this information, artist list and timetable will be generated.<br>
Please pick up the ids by searching solo player(member) or band.<br><br>
band,bandid,duration(min)<br>
or<br>
slot,free text,duration(min)<br>
or<br>
member,memberid,duration(min)<br>
<br>
ex)<br>
band,1,10<br>
slot,set change,10<br>
member,1,5<br>
<br>

